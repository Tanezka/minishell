int
	arg_operator(const struct s_arg arg)
{
	if (arg.operator && (\
		*(arg.this) == '<' || \
		*(arg.this) == '>' || \
		*(arg.this) == '|' || \
		*(arg.this) == ';'))
		return (1);
	return (0);
}
