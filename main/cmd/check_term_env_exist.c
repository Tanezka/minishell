/* **************************** [v] INCLUDES [v] **************************** */
#include <term.h> /*
@ +----+ +-----------+
@ |FLAG| | -lncurses |
@ +----+ +-----------+
#    int tgetent(char *, char *);
#        */
#include <stdlib.h> /*
#   char *getenv(char *);
#        */
/* **************************** [^] INCLUDES [^] **************************** */

int
	check_term_env_exist(void)
{
	if (tgetent(((void *)0), getenv("TERM")) < 1)
		return (0);
	return (1);
}
